package com.example.dangineering.gettytwo.presentation.viewmodel;

public class ImageViewModel {

    String url;
    String title;

    public ImageViewModel(String url, String title) {
        this.url = url;
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
